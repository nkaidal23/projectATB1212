package JavaBasics_009_forloop;

public class java_73_forifloop {
    public static void main(String[] args) {
        for (int anishka = 0; anishka < 20; anishka++) {
            if (anishka > 18) {
                System.out.println("gift iphone");
            } else {
                System.out.println("no gift");
            }
        }
    }
}