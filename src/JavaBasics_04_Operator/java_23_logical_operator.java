package JavaBasics_04_Operator;

public class java_23_logical_operator {
    public static void main(String[] args) {
       //&& AND 1 1 = 1
        // || OR 0 0 = 0
        // ! NOT GATE

        boolean c = true&&true;
        System.out.println(c);

        boolean a = false||false;
        System.out.println(a);

        boolean b = !true;
        System.out.println(b);
    }
}
