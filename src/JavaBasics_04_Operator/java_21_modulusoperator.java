package JavaBasics_04_Operator;

public class java_21_modulusoperator {
    public static void main(String[] args) {
        int a = 30;
        int b = 10;
        System.out.println(a%b);
        //mod--remainder
        //30/10 = 3 - quotient 0 = remainder

        //10/2-0 remainder even
        //12/2 - 0 remainder even
        //13/2--1 remainder odd

        //any number % 2
        // r = 0  its even number,
          //      r = 1 its odd number
    }
}
