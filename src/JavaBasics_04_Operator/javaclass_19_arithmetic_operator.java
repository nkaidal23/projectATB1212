package JavaBasics_04_Operator;

public class javaclass_19_arithmetic_operator {
    public static void main(String[] args) {
        //Addition +
        //subtraction _
            //    multiplication *
              //  division /
                //        modulus | remainder %
        int a = 10;
        int b = 12;
        float c = 3.44f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a%b);

        System.out.println(a/c);
        System.out.println("a+b");
    }

}
