package JavaBasics_03_Literals;

public class literals_14_boolean {
    public static void main(String[] args) {
        int age = 12;
        System.out.println(age);
        //literal = integral
         final float height = 3.14f;
         //literal = float
        System.out.println(height);

        final boolean b = true ;
//literal = boolean
        System.out.println(b);

    }
}
